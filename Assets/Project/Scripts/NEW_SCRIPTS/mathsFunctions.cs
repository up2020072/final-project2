using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class TilePosition
{
    public float x, y;
    public TilePosition()
    {
        this.x = 0;
        this.y = 0;
    }
    public TilePosition(float x, float y)
    {
        this.x = x;
        this.y = y;
    }
}
